<h1>Impact partners with the Chapel at Howard</h1>
<p>We often notice an increase in crime and theft during the holiday season. To ensure you are not a victim, 
please take a few minutes to review some travel tips to keep your mobile devices and personal information safe 
and secure during the holidays! Remember to travel with the minimum number of devices!
</p>
<ul>
<li>Password-protect your device - Make sure that you have to enter a password to log in to your device.
</li>
<li>Keep your valuables with you at all times - When traveling, keep your device with you. Meal times are 
optimum times for thieves to check hotel rooms for unattended laptops.
</li> 
<li>Downplay your laptop or mobile device - There is no need to advertise to thieves that you have a laptop
 or mobile device. Avoid using your device in public areas, and consider non-traditional bags for carrying 
 your laptop.
 </li>
<li>Be aware of your surroundings - If you do use your laptop or mobile device in a public area, pay attention 
to people around you. Take precautions to shield yourself from "shoulder surfers"—make sure that no one can see 
you type your passwords or see any sensitive information on your screen.
</li>
<li>Do not use public Wi-Fi networks - Avoid using open Wi-Fi networks especially to conduct personal business, 
bank, or shop online. Open Wi-Fi networks at places such as airports, coffee shops, and other public locations 
present an opportunity for attackers to intercept sensitive information that you would provide to complete an 
online transaction. If you simply must check your bank balance or make an online purchase while you are traveling, 
turn off your device's Wi-Fi connection and use your mobile device's cellular data internet connection.
</li> 
<li>Turn off Bluetooth when not in use - Bluetooth-enabled accessories can be helpful and convenient but when these 
devices are not in use, turn off the Bluetooth setting on your phone. Cyber criminals have the capability to pair 
with your phone's open Bluetooth connection when you are not using it and steal personal information. In addition, 
turning off the Bluetooth will also save battery life of your device.
</li>
<li>Be cautious when charging - Avoid connecting your mobile device to any computer or charging station that you 
do not control, such as a charging station at an airport terminal or a shared computer at a library. Connecting a 
mobile device to a computer using a USB cable can allow malicious software running on that computer to interact with 
the phone in ways that a user may not anticipate. As a result, a malicious hacker could gain access to your sensitive 
data or install new software.
</li> 
<li>Don't Fall Victim to Phishing Scams - If you are in the shopping mode, an email that appears to be from a 
legitimate retailer might be difficult to resist. If the deal looks too good to be true, or the link in the email 
or attachment to the text seems suspicious, do not click on it!
</li>
<li>What to do if your accounts are compromised - If you notice that one of your online accounts has been hacked, 
call the bank, store, or credit card company that owns your account. Reporting fraud in a timely manner helps minimize 
the impact and lessens your personal liability. You should also change your account passwords for any online services 
associated with your mobile device using a different computer that you control. If you are the victim of identity theft, 
additional information is available from http://www.idtheft.gov/.
</li>
</ul>
<p>Have a safe and Happy Holiday!</p>